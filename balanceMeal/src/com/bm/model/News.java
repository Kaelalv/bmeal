package com.bm.model;

public class News {
	private Integer id;
	private String newstit;
	private String newsintro;
	private String newsimg;
	private String newsdetail;
	private String newspublish;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNewstit() {
		return newstit;
	}
	public void setNewstit(String newstit) {
		this.newstit = newstit;
	}
	public String getNewsintro() {
		return newsintro;
	}
	public void setNewsintro(String newsintro) {
		this.newsintro = newsintro;
	}
	public String getNewsimg() {
		return newsimg;
	}
	public void setNewsimg(String newsimg) {
		this.newsimg = newsimg;
	}
	public String getNewsdetail() {
		return newsdetail;
	}
	public void setNewsdetail(String newsdetail) {
		this.newsdetail = newsdetail;
	}
	public String getNewspublish() {
		return newspublish;
	}
	public void setNewspublish(String newspublish) {
		this.newspublish = newspublish;
	}
	
	
}
